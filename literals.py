# Python literals are the notation for representing fixed value in source code. They are used to denote values of various data types such as strings, numbers, booleans, and more. Here are some examples of different types of literals in Python:
# String literals
string_literal = "Hello, World!"
# Integer literals
integer_literal = 42
# Float literals
float_literal = 3.14
# Boolean literals
boolean_literal = True
# None literal
none_literal = None
# List literals
list_literal = [1, 2, 3, "apple", "banana"]
# Tuple literals
tuple_literal = (1, 2, 3, "apple", "banana")
# Dictionary literals
dict_literal = {"name": "Alice", "age": 30}
# Set literals
set_literal = {"apple", "banana", "cherry"}
# Python also supports various types of numeric literals, including:
# Binary literals (base 2)  
binary_literal = 0b1010  # Represents the binary number 1010 (10 in decimal)
# Octal literals (base 8)   
octal_literal = 0o52  # Represents the octal number 52 (42 in decimal)
# Hexadecimal literals (base 16)
hexadecimal_literal = 0x2A  # Represents the hexadecimal number 2A (42 in decimal)
# Python also supports complex number literals, which consist of a real part and an imaginary part.
complex_literal = 3 + 4j  # Represents the complex number 3 + 4j    
# Python literals can also include escape sequences, which are used to represent special characters in strings. For example:
escaped_string_literal = "Hello, \"World\"!\nWelcome to Python."    
# In this example, the escape sequence \" is used to include double quotes within the string, and \n is used to create a new line.
